#include"FillRand.h"

void FillRand(int c_arr[ROWS][COLS], const int ROWS, const int COLS)
{
	for (int i = 0; i < ROWS; i++)
	{
		for (int j = 0; j < COLS; j++)
		{
			c_arr[i][j] = rand() % 100;
		}
	}
}
